// @details	
//
// 810092048
// 0x30490610
// 0b00110000010010010000011000010000 t
//   --------------------------------
//   16151413121110090807060504030201
//   --------------------------------
//                                 11 t &3      <-----+
//                                 xx c               |
//   00161514131211100908070605040302 t >> 2          |
//   00xx1514131211100908070605040302 t | c << 28   --+
//   --------------------------------
//   33222222222211111111110000000000
//   10987654321098765432109876543210
//

#include	<stdio.h>

int main(void)
{
	const char	*fmt[] = {"%d ", "Fizz ", "Buzz ", "FizzBuzz "};
	int i = 0, c, t = 810092048;	// 0x30490610
	
	while (i++ ^ 100) {
		printf(fmt[c = t & 3], i);
		t = t >> 2 | c << 28;
	}
	puts("");
}
